#Operadores de comparação

#Inicialização de variaveis
variavel_a = 10
variavel_b = 5

#Execução dos operadores de comparação

igual = variavel_a == variavel_b
print("Operador de comparação de igualdade:", igual)

diferente = variavel_a != variavel_b
print("Operador de comparação de desigualdade:", diferente)

maior = variavel_a > variavel_b
print("Operador de comparação de maior que:", maior)

menor = variavel_a < variavel_b
print("Operador de comparação de menor que:", menor)

maior_igual = variavel_a >= variavel_b
print("Operador de comparação de maior ou igual:", maior_igual)

menor_igual = variavel_a <= variavel_b
print("Operador de comparação de menor ou igual:", menor_igual)